    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        ListaProyectosInvestigativos LAP = new ListaProyectosInvestigativos();
        StudentsFrame ST = new StudentsFrame();
        
        try {
            LAP.GuardarPractica(this.jTextArea1.getText() + " | ", this.jTextField1.getText() + " d√≠as | ",
                    this.jTextField2.getText() + " | ", this.jTextArea2.getText() + " | ",
                    this.jComboBox1.getSelectedItem().toString() + " | ", this.jComboBox2.getSelectedItem().toString() + " | ",
                    this.jTextArea3.getText() + " | ", this.jTextArea5.getText() + " | ", this.jLabel11.getText() + " | ");
        } catch (Exception ex) {
            Logger.getLogger(PanelRegistrarPropuesta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(ST, "***** PROYECTO REGISTRADO CON EXITO *****",
            "ID de Radicado: " /*+ ListaProyectos.getProyectoInvestigativo().getIdRadicado() + " *****"*/,
            JOptionPane.INFORMATION_MESSAGE);
        
        Limpiar();
    }//GEN-LAST:event_jButton1MouseClicked

