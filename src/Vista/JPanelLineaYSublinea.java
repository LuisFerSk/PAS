    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if(this.Num == 1){
            if(!(jComboBox1.getSelectedItem().toString().equals("Seleccionar..."))){
                jTextField1.setText("Crear " + jComboBox1.getSelectedItem().toString());
                jTextField9.setText("Nombre de " + jComboBox1.getSelectedItem().toString() + ":");
                jButton6.setText("Registrar");
                if((jComboBox1.getSelectedItem().toString().equals("Linea Investigativa")) || (jComboBox1.getSelectedItem().toString().equals("Linea Investigativa"))){
                    jSeparator2.setVisible(true);
                    jTextField9.setVisible(true);
                    jTextField4.setVisible(true);
                    jButton6.setVisible(true);
                }
            }else{
                jTextField9.setVisible(false);
                jTextField4.setVisible(false);
                jSeparator2.setVisible(false);
                jButton6.setVisible(false);
            }
        }else{
            if((this.Num == 2) || (this.Num == 3) || (this.Num == 4)){
                jTextField1.setVisible(true);
                jTextField4.setVisible(true);
                jTextField9.setVisible(true);
                jSeparator2.setVisible(true);
                jComboBox1.setVisible(true);
                jButton6.setVisible(true);

                if(this.Num == 2 && this.btn == 2){
                    jTextField1.setText("Buscar " + jComboBox1.getSelectedItem().toString());
                    jButton6.setText("Buscar");
                }else{
                    if(this.Num == 3 && this.btn == 3){
                        jTextField1.setText("Eliminar " + jComboBox1.getSelectedItem().toString());
                        jButton6.setText("Eliminar");
                    }else{
                        jTextField1.setText("Actualizar " + jComboBox1.getSelectedItem().toString());
                        jButton6.setText("Actualizar");
                    }
                }

                if(!(jComboBox1.getSelectedItem().toString().equals("Seleccionar...")))
//                    if(jComboBox1.getSelectedItem().toString().equals("Linea Investigativa"))
                    jTextField9.setText("ID de " + jComboBox1.getSelectedItem().toString() + ": ");
//                    else
//                    jTextField9.setText("ID de " + jComboBox1.getSelectedItem().toString() + ":");
                else{
                    jTextField9.setVisible(false);
                    jTextField4.setVisible(false);
                    jButton6.setVisible(false);
                }
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

